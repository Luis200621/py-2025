# Declaración de variables
numero_entero = 10
numero_decimal = 3.14
texto = "Hola, luis"

# Impresión de los valores
print("Número entero:", numero_entero)
print("Número decimal:", numero_decimal)
print("Texto:", texto)

# Verificación del tipo de dato de cada variable
print("Tipo de numero_entero:", type(numero_entero))
print("Tipo de numero_decimal:", type(numero_decimal))
print("Tipo de texto:", type(texto))
